    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        switch (evt.getKeyCode()) {
            case LEFT_KEY_CODE:
                leftPressed = true;
                leftAsserted = 1;
                rightPressed = false;
                rightAsserted = 0;
                break;
            case RIGHT_KEY_CODE:
                rightPressed = true;
                rightAsserted = 1;
                leftPressed = false;
                leftAsserted = 0;
                break;
            case START_KEY_CODE:
                startPressed = true;
                break;
            default:
                rotatePressed = true;
                break;
        }
    }//GEN-LAST:event_formKeyPressed

    private void formKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyReleased
        switch (evt.getKeyCode()) {
            case LEFT_KEY_CODE:
                leftPressed = false;
                if (leftAsserted == 2) {
                    leftAsserted = 0;
                }
                break;
            case RIGHT_KEY_CODE:
                rightPressed = false;
                if (rightAsserted == 2) {
                    rightAsserted = 0;
                }
                break;
        }
    }//GEN-LAST:event_formKeyReleased

